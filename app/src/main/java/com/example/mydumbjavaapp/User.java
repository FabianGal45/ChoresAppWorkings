package com.example.mydumbjavaapp;

public class User {
    private String name;

    //Constructors
    public User() {
    }

    public User(String name) {
        this.name = name;
    }

    //Getters and setters
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
